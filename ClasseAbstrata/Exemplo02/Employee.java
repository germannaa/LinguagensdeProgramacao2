
public abstract class Employee {
    public String name;
    public double salary;

    public Employee() {
        this.name = name;
        this.salary = salary;
    }

    public String getName() {
        return name;
    }

    public double getSalary() {
        return salary;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }
    
    public abstract void increaseSalary();
    
}
